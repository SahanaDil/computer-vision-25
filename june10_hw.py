#1 sum of the first n odd numbers
def sum_first_n_odds(n):
    print(n ** 2)

sum_first_n_odds(4)
sum_first_n_odds(7)

#2 sum of squares
def sum_squares(n):
    print ((n * (n + 1) * (2 * n + 1))/6)

sum_squares(3)
sum_squares(5)

#3 sum of cubes
def sum_cubes(n):
    print((n * (n + 1) / 2) ** 2)

sum_cubes(2)
sum_cubes(4)

#4 average of 3 numbers
def average(A, B, C):
    print((A + B + C) / 3)

average(4,11,3)
average(-2,8,6)

